public class Variables {
    public static void main(String[] args) {

        // Tipo entero: para números sin decimales
        int edad = 15;

        // Tipo texto: siempre entre comillas dobles
        String nombre = "Ana";

        // Tipo decimal: para números con punto decimal
        double altura = 1.65;

        // Tipo booleano: solo puede ser true o false
        boolean meGustaJava = true;

        // Mostramos todos los valores en pantalla
        System.out.println("Nombre: " + nombre);
        System.out.println("Edad: " + edad + " años");
        System.out.println("Altura: " + altura + " metros");
        System.out.println("¿Me gusta Java? " + meGustaJava);
    }
}
